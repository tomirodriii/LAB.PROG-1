#3. Escribe un programa que realice operaciones aritméticas simples, 
#como suma, resta, multiplicación y división, utilizando números enteros y flotantes.

num1 = int(input("Ingrese un numero:"))
num2 = int(input("Ingrese otro numero:"))

sumados = num1 + num2
restados = num1 - num2
multiplicados = num1 * num2
divididos = num1 / num2

print(f"Esos numeros sumados dan: {sumados}")
print(f"Esos numeros restados dan: {restados}")
print(f"Esos numeros multiplicados dan: {multiplicados}")
print(f"Esos numeros divididos dan: {divididos}")