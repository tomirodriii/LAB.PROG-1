# 5. Escribe un programa que compare dos números y muestre si son iguales o no utilizando operadores 
# de comparación (por ejemplo, ==, !=)

num1 = int(input("Ingrese un numero:"))
num2 = int(input("Ingrese otro numero:"))

if num1 == num2:
    print(f"El numero {num1} es igual al {num2}")
else:
    print(f"El numero {num1} es diferente al {num2}")